function t(key, ...args) {
  let s = (I18N[_lang] || I18N.uk)[key];
  if (s === undefined) s = I18N.uk[key] || key;
  args.forEach(a => {
    const str = String(a);
    if (s.includes('%s')) s = s.replace('%s', str);
  });
  return s;
}

function getLang() { return _lang; }

function setLang(lang) {
  if (!I18N[lang]) return;
  _lang = lang;
  document.documentElement.lang = lang === 'uk' ? 'uk' : 'en';
  applyI18n();
  if (typeof window.__strucodeOnLangChange === 'function') {
    try { window.__strucodeOnLangChange(lang); } catch (e) { console.warn('lang hook', e); }
  }
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t(key);
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const k = el.dataset.i18nPlaceholder;
    if (k) el.placeholder = t(k);
  });
  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const k = el.dataset.i18nAriaLabel;
    if (k) el.setAttribute('aria-label', t(k));
  });
}
