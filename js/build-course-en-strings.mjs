/**
 * Builds js/courses-en-strings.js from course-titles.tsv + js/course-en-lines.txt
 * Run: node js/build-course-en-strings.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const tsvPath = path.join(root, 'course-titles.tsv');
const linesPath = path.join(__dirname, 'course-en-lines.txt');
const outPath = path.join(__dirname, 'courses-en-strings.js');

const tsvLines = fs.readFileSync(tsvPath, 'utf8').trim().split(/\n/);
const tsv = tsvLines.map(l => l.split('\t'));
const enLines = fs.readFileSync(linesPath, 'utf8').trim().split(/\r?\n/).map(s => s.replace(/\r/g, '').trimEnd());

if (enLines.length !== tsv.length) {
  console.error(`Mismatch: tsv ${tsv.length} lines vs en ${enLines.length}`);
  process.exit(1);
}

function rowToKey(row) {
  const [type, course, col3] = row;
  if (type === 'C') return `${course}||desc`;
  if (type === 'M') return `${course}|${col3}`;
  if (type === 'L') {
    const parts = String(col3 || '').split('|');
    if (parts.length >= 2) return `${course}|${parts[0]}|${parts[1]}`;
  }
  return '';
}

const keys = tsv.map(rowToKey);

const obj = {};
keys.forEach((k, i) => {
  obj[k] = enLines[i];
});

const body = `// Auto-generated by js/build-course-en-strings.mjs — do not edit by hand; edit course-en-lines.txt and re-run.\nwindow.COURSE_EN_PLAIN = ${JSON.stringify(obj, null, 0)};\n`;
fs.writeFileSync(outPath, body, 'utf8');
console.log('Wrote', outPath, Object.keys(obj).length, 'keys');
